<script lang="ts">
import { defineComponent, PropType, ref } from "vue";
import { TableData } from "@/components/patients/health-concerns/HealthConcerns.vue";
import ActionButton from "@/components/patients/ActionButton.vue";

export type ConcernAction = "view" | "mark-as-resolved" | "promote-to-problem" | "remove";

export default defineComponent({
	name: "HealthConcernsTable",
	components: {
		ActionButton
	},
	props: {
		data: {
			type: Array as PropType<TableData[]>,
			required: true
		},
		title: {
			type: String,
			default: "Active Health Concerns"
		},
		type: {
			type: String,
			required: true
		}
	},
	emits: ["add-concern", "trigger-open-assessment", "concern-action"],
	setup(props, { emit }) {
		const concernOrActionClick = (id: string, action: ConcernAction) => {
			emit("concern-action", { id, action });
		};

		return {
			concernOrActionClick
		};
	}
});
</script>

<template>
	<div
		class="table-wrapper"
	>
		<div class="header">
			<div class="title">
				<h3>
					{{ title }}
				</h3>
			</div>
			<el-button
				v-if="type === 'active'"
				plain
				round
				type="primary"
				size="mini"
				@click="$emit('add-concern')"
			>
				Add Health Concern
			</el-button>
		</div>
		<el-table :data="data">
			<el-table-column
				label="Health Concern"
			>
				<template #default="scope">
					<el-button
						type="text"
						@click="concernOrActionClick(scope.row.id, 'view')"
					>
						{{ scope.row.name }}
					</el-button>
				</template>
			</el-table-column>
			<el-table-column
				label="Category"
			>
				<template #default="scope">
					{{ scope.row.category.display }}
				</template>
			</el-table-column>
			<el-table-column
				label="Based On"
			>
				<template #default="scope">
					{{ scope.row.basedOn.display ? scope.row.basedOn.display : scope.row.basedOn }}
					<span
						v-if="scope.row.basedOn.id"
						class="icon-link"
						@click="$emit('trigger-open-assessment', scope.row.basedOn.id)"
					>
					</span>
				</template>
			</el-table-column>
			<el-table-column
				label="Assessment Date"
			>
				<template #default="scope">
					{{ scope.row.assessmentDate ? $filters.formatDateTime(scope.row.assessmentDate) : "N/A" }}
				</template>
			</el-table-column>
			<el-table-column
				v-if="type === 'resolved'"
				label="Resolution Date"
				width="350"
			>
				<template #default="scope">
					{{ scope.row.resolutionDate ? $filters.formatDateTime(scope.row.resolutionDate) : "N/A" }}
				</template>
			</el-table-column>
			<el-table-column
				v-if="type === 'active'"
				label="Actions"
				width="350"
			>
				<template #default="scope">
					<ActionButton
						icon-class="icon-promote"
						label="Promote to Problem"
						@clicked="concernOrActionClick(scope.row.id, 'promote-to-problem')"
					/>
					<ActionButton
						icon-class="icon-resolved"
						label="Mark as Resolved"
						@clicked="concernOrActionClick(scope.row.id, 'mark-as-resolved')"
					/>
					<ActionButton
						icon-class="icon-remove"
						label="Remove"
						@clicked="concernOrActionClick(scope.row.id, 'remove')"
					/>
				</template>
			</el-table-column>
		</el-table>
	</div>
</template>

